// RAM[4] = (RAM[0] * 3 + (RAM[1] | RAM[2])) & !RAM[3] + 11

// Загружаем значения из памяти
@0
D=M          // D = RAM[0]

// Умножаем RAM[0] на 3 (RAM[0] * 2 + RAM[0])
@16
M=D          // RAM[16] = RAM[0]
D=D+M        // D = RAM[0] * 2
@16
D=D+M        // D = RAM[0] * 3
@17
M=D          // RAM[17] = RAM[0] * 3

// Вычисляем (RAM[1] | RAM[2])
@1
D=M          // D = RAM[1]
@2
D=D|M        // D = RAM[1] | RAM[2]
@18
M=D          // RAM[18] = RAM[1] | RAM[2]

// Складываем результаты: RAM[0]*3 + (RAM[1]|RAM[2])
@17
D=M          // D = RAM[0]*3
@18
D=D+M        // D = RAM[0]*3 + (RAM[1]|RAM[2])
@19
M=D          // RAM[19] = промежуточный результат

// Вычисляем !RAM[3] (побитовое НЕ)
@3
D=M          // D = RAM[3]
D=!D         // D = !RAM[3]
@20
M=D          // RAM[20] = !RAM[3]

// Выполняем побитовое И: (вышеуказанная сумма) & !RAM[3]
@19
D=M          // D = RAM[0]*3 + (RAM[1]|RAM[2])
@20
D=D&M        // D = (RAM[0]*3 + (RAM[1]|RAM[2])) & !RAM[3]

// Прибавляем 11
@11
D=D+A        // D = результат + 11

// Сохраняем в RAM[4]
@4
M=D          // RAM[4] = окончательный результат